    <h2>Inilah Daftar 7 Komik Jepang Terlaris di Dunia</h2>
    <p> Komik Jepang alias Manga memang sudah menjadi konsumsi masyarakat umum dunia. Beberapa di antara komik jepang yang ada telah mencatat rekor spektakuler dalam panjang episodenya, namun apakah panjang episode berpengaruh ke jumlah penjualan komik tersebut? Ternyata tidak. Berikut adalah daftar 7 komik jepang terlaris dalam sejarah (data hingga akhir 2014), beberapa di antaranya bahkan tamat sebelum edisi ke-50.</p>
    </div>
    <div class="main">
      <img src="img/conan.jpg">
    <h3>7. Detektif Conan</h3>
    <p> Salah satu komik jepang terlaris yang juga diminati banyak pembaca Indonesia adalah Detektif Conan. Manga yang hingga sekarang masih berlanjut ini menceritakan tentang seorang detektif remaja, Sinichi Kudo yang tidak mati ketika diracun, namun tubuhnya menyusut menjadi seukuran anak kecil. Sejak menjadi kecil, Sinichi Kudo kemudian mengubah namanya menjadi Conan Edogawa dan melanjutkan petualangannya bersama sang kekasih, Ran Mouri dan ayahnya yang juga detektif, Kogoro Mouri. Manga karya Aoyama Gosho ini telah terjual lebih dari 140 juta eksemplar di seluruh dunia. Jumlah tersebut tentu masih terus bertambah mengingat hingga tulisan ini dibuat, manga Detektif Conan masih berlanjut penerbitannya.</p>
    </div>

    <div class="main">
      <img src="img/Kochikame.jpg">
    <h3>6. Kochikame</h3>
    <p> Memiliki judul lengkap Kochira Katsushika-ku Kameari Koen Mae Hashutsujo (Inilah Pos Polisi di Seberang Taman Kameari di Wilayah Katsushika), komik jepang karya Osamu Akimoto ini telah terbit selama hampir 40 tahun. Muncul pertama kali di Weekly Shonen Jump pada September 1976, manga yang masih terbit ini telah memiliki lebih dari 190 edisi dan telah terjual lebih dari 155 juta eksemplar, sangat wajar karena rentang terbitnya yang sudah cukup panjang. Manga ini termasuk dalam daftar 3 manga terpanjang dalam sejarah. Kochikame sendiri bercerita tentang seorang polisi paruh baya, Kankichi “Ryo-san” Ryotsu dalam menjalani pekerjaan sehari-harinya sebagai polisi lingkungan (koban) di bagian pusat kota Tokyo. Kochikame bergenre komedi dan disebut-sebut cocok untuk semua umur, walau sesekali terdapat adegan tak senonoh di dalamnya.</p>
    </div>

    <div class="main">
      <img src="img/jack.jpg">
    <h3>5. Black Jack</h3>
    <p> Black Jack adalah salah satu karya masterpiece Osamu Tezuka, sang Bapak Komik Jepang, selain Astro Boy. Walau hanya terbit sebanyak 17 edisi, penjualan Black Jack telah mencapai 176 juta eksemplar dan hingga sekarang masih dibuat remakenya oleh mangaka lain. Black Jack terbit selama kurun 10 tahun sejak 1973 hingga 1983, menceritakan tentang petualangan seorang dokter dalam menyelamatkan pasien-pasiennya. Selain manga, Black Jack juga telah dibuat dalam versi OVA, anime dan live action.</p>
    </div>

    <div class="main">
      <img src="img/golgo.jpg">
    <h3>4. Golgo 13</h3>
    <p> Judul aslinya adalah Gorugo Satin, manga karya Takao Saito ini telah terbit selama hampir 50 tahun. Muncul pertama kali di majalah Big Comic tahun 1968, kini komik jepang Golgo 13 telah memiliki lebih dari 170 edisi dan terjual lebih dari 200 juta eksemplar. Golgo 13 juga telah diadaptasi dalam bentuk serial anime dan juga film animasi maupun live-action.Manga Golgo 13 menceritakan tentang petualangan seorang pembunuh profesional dalam menjalankan misi-misinya. Nama asli sang tokoh utama adalah Duke Togo (Dyuku Togo) dan nickname Golgo 13 sendiri terinspirasi dari tempat penyaliban Yesus, Golgota.</p>
    </div>

    <div class="main">
      <img src="img/naruto.jpg">
    <h3>3. Naruto</h3>
    <p> Siapa yang tak kenal Naruto? Komik jepang tentang anak yang dijadikan tempat menyegel rubah ekor sembilan ini telah terjual lebih dari 205 juta eksemplar dan tamat pada edisi ke-72. Naruto adalah manga karya Masashi Kishimoto tentang dunia para ninja yang penuh intrik, kisah persahabatan dan dendam. Manga Naruto telah mendapat banyak penghargaan baik nasional (Jepang) maupun internasional dan menjadi langganan 10 pencarian kata kunci artikel maupun gambar terpopuler di seluruh duni</p>
    </div>

    <div class="main">
      <img src="img/ball.jpg">
    <h3>2. Dragon Ball</h3>
    <p> Jauh sebelum Naruto eksis, generasi 80an sangat menggilai komik jepang ini. Dragon Ball sempat merajai penjualan manga dunia dan telah terjual lebih dari 230 juta eksemplar. Terbit selama tahun 1984 hingga 1995, Dragon Ball hanya terbit sebanyak 42 edisi namun buah karya Akira Toriyama ini tak terkalahkan penjualannya selama bertahun-tahun hingga akhirnya disalip oleh peringkat pertama dalam daftar komik jepang terlaris. Manga Dragon Ball berlatar kisah dari Journey to The West namun dengan pengembangan yang sangat futuristik. Bercerita tentang petualangan Son Goku dalam mengumpulkan bola naga berbintang (Dragon Ball) yang dapat mengabulkan permintaan, Goku dipertemukan dengan lawan-lawan tangguh seperti Pikoro (Piccolo), Bezita (Vegeta), Freeza (Frieza), Cell dan Majin Buu.</p>
    </div>

    <div class="main">
      <img src="img/one.jpg">
    <h3>1. One Piece</h3>
    <p> Pada tahun 2014, manga One Piece tercatat dalam Guinness World Record sebagai “Komik dengan jumlah eksemplar cetak terbanyak, yang ditulis oleh satu orang” (the most copies published for the same comic book series by a single author). Saat mendapatkan penghargaan itu, komik jepang karya Eiichiro Oda tersebut telah terjual sebanyak 320.866.000 eksemplar di seluruh dunia. One Piece meninggalkan jauh pesaing terdekatnya, Dragon Ball yang sebelumnya pernah menjadi komik dengan penjualan terbanyak selama bertahun-tahun. One Piece bercerita tentang petualangan Monkey D. Luffy mengarungi lautan, mengumpulkan kru bajak laut pimpinannya sendiri, untuk mengejar harta terbesar bajak laut yang disebut dengan One Piece. Hingga tulisan ini dibuat, manga One Piece masih berlanjut, membuatnya akan bertahan lama di tingkat teratas komik jepang terlaris di dunia.</p>
    </div>
